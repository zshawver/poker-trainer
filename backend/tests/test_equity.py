def test_equity_vs_hand_preflop_aa_vs_kk_monte_carlo() -> None:
    """Live preflop AA-vs-KK lands in the textbook 0.79-0.84 band."""
    result = equity_vs_hand(
        ["As", "Ad"], ["Ks", "Kd"],
        board=None,
        n_samples=5000,
    )
    assert 0.79 < result["equity"] < 0.84, (
        f"AA vs KK preflop = {result['equity']:.4f} (expected ~0.81)"
    )
    # Probabilities partition the outcome space.
    assert abs(
        result["win"] + result["tie"] + result["lose"] - 1.0
    ) < 1e-9


def test_equity_vs_range_postflop_river_exact() -> None:
    """Hero with the nuts on the river beats every hand in a strong range."""
    # Hero As Ks on Qs Js Ts 2c 3d -> royal flush. Villain range is sets and
    # two pair candidates that all lose to the royal.
    result = equity_vs_range(
        ["As", "Ks"], ["QQ", "JJ", "TT"],
        board=["Qs", "Js", "Ts", "2c", "3d"],
    )
    assert result["equity"] == 1.0
    assert set(result["by_hand"].keys()) == {"QQ", "JJ", "TT"}
    for ht in ("QQ", "JJ", "TT"):
        assert result["by_hand"][ht] == 1.0
